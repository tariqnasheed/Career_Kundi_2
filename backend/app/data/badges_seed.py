"""
data/badges_seed.py
====================
Authoritative definition of every badge in the CareerKundi system.

Each entry maps to a row in `badge_definitions`. The data is consumed by
`app/services/badges.py::seed_badge_definitions()` at server startup (idempotent
upsert on the string `id`).

Design principles:
  - Badges represent genuine career progress, not vanity actions.
  - Names are professional; descriptions are motivational but not patronising.
  - Rarity matches effort: most users will earn `common` badges; `legendary`
    requires sustained, exceptional engagement.
  - condition_metric values are the internal event names fired by badge_service.
"""

