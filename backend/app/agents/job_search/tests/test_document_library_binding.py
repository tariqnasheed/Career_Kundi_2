# --- T5 — no document library (deterministic) -----------------------------------

def test_t5_missing_library_is_deterministic(tmp_path: Path) -> None:
    missing = tmp_path / "nope"
    fp1 = compute_document_library_fingerprint(missing)
    fp2 = compute_document_library_fingerprint(missing)
    assert fp1 == fp2 and len(fp1) == 16
    # An empty (but existing) library is distinct from a populated one.
    empty = tmp_path / "empty_docs"
    empty.mkdir()
    populated = tmp_path / "populated" / "documents"
    _make_library(populated)
    assert compute_document_library_fingerprint(empty) != compute_document_library_fingerprint(
        populated
    )


# --- T6 — corrected dependency evidence (replaces the false T11 string-scan) -----

def test_t6_generation_call_graph_reads_document_library() -> None:
    """Dependency-aware proof that generation reads documents/ (the premise the
    old script-string-scan T11 got wrong)."""
    import builtins  # noqa: F401
    reads: set[str] = set()
    _rt = Path.read_text

    def traced(self, *a, **k):  # type: ignore[no-untyped-def]
        s = str(self)
        if "/documents/" in s:
            reads.add(s.split("/documents/", 1)[1])
        return _rt(self, *a, **k)

    Path.read_text = traced  # type: ignore[assignment]
    try:
        from app.agents.job_search.knowledge.source_ladder import build_source_ladder_status

        build_source_ladder_status({"title": "Graphic Designer"})
    finally:
        Path.read_text = _rt  # type: ignore[assignment]
    # Generation genuinely reads the document library (the premise the old
    # script-string-scan T11 got wrong)...
    assert reads, "generation must consume document-library files"
    # ...and every consumed file falls within the BOUND fingerprint scope
    # (indexes/ or interview_packs/, json/md only) — so the binding covers the
    # real dependency and nothing consumed escapes it.
    for rel in reads:
        assert rel.startswith(("indexes/", "interview_packs/")), rel
        assert rel.endswith((".json", ".md")), rel


# --- Run-context integration ----------------------------------------------------

def test_manifest_carries_document_library_fingerprint() -> None:
    manifest = build_generation_run_manifest(REPO_ROOT)
    assert manifest.get("document_library_fingerprint")
    assert len(manifest["document_library_fingerprint"]) == 16


def test_document_fingerprint_participates_in_context_digest() -> None:
    manifest = build_generation_run_manifest(REPO_ROOT)
    other = dict(manifest)
    other["document_library_fingerprint"] = "0" * 16
    assert context_digest_of(manifest) != context_digest_of(other)


def test_document_library_change_marks_stored_artifact_stale() -> None:
    manifest = build_generation_run_manifest(REPO_ROOT)
    context = current_run_context(REPO_ROOT)
    # Same state -> current.
    assert classify_artifact_evidence(manifest, context)["status"] == STATUS_CURRENT
    # Different document-library fingerprint -> stale (old evidence not current).
    drifted = dict(context)
    drifted["document_library_fingerprint"] = "deadbeefdeadbeef"
    result = classify_artifact_evidence(manifest, drifted)
    assert result["status"] == STATUS_STALE
    assert REASON_DOCUMENT_LIBRARY_CHANGED in result["reasons"]
